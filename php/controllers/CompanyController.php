<?php

namespace app\controllers;

use app\models\SignupForm;
use app\models\User;
use Yii;
use app\models\Company;
use yii\data\ActiveDataProvider;
use yii\web\Controller;
use yii\web\HttpException;
use yii\web\NotFoundHttpException;
use yii\filters\VerbFilter;

/**
 * CompanyController implements the CRUD actions for Company model.
 */
class CompanyController extends Controller
{
    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'delete' => ['POST'],
                ],
            ],
        ];
    }

    public function beforeAction($action)
    {
        if (!Yii::$app->user->can('manageCompany')) {
            throw new HttpException(403, 'У вас нет доступа к этой странице');
        }
        return parent::beforeAction($action); // TODO: Change the autogenerated stub
    }

    /**
     * Lists all Company models.
     * @return mixed
     */
    public function actionIndex()
    {
        $dataProvider = new ActiveDataProvider([
            'query' => Company::find()->where('status != '.Company::STATUS_DELETED),
        ]);

        return $this->render('index', [
            'dataProvider' => $dataProvider,
        ]);
    }

    /**
     * Lists all Company models.
     * @return mixed
     */
    public function actionView($id)
    {
        $company = $this->findModel($id);
        $usersProvider = new ActiveDataProvider([
            'query' => User::find()->where(['company_id' => $id, 'status' => User::STATUS_ACTIVE])
        ]);

        return $this->render('view', [
            'model' => $company,
            'usersProvider' => $usersProvider
        ]);
    }

    /**
     * Creates a new Company model.
     * If creation is successful, the browser will be redirected to the 'redirect' page.
     * @return mixed
     */
    public function actionCreate()
    {
        $model = new Company();

        if ($model->load(Yii::$app->request->post()) && $model->save()) {
            return $this->redirect(['index']);
        }

        return $this->render('create', [
            'model' => $model,
        ]);
    }

    /**
     * Updates an existing Company model.
     * If update is successful, the browser will be redirected to the 'redirect' page.
     * @param integer $id
     * @return mixed
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionUpdate($id)
    {
        $model = $this->findModel($id);

        if ($model->load(Yii::$app->request->post()) && $model->save()) {
            return $this->redirect(['redirect']);
        }

        return $this->render('update', [
            'model' => $model,
        ]);
    }

    /**
     * Deletes an existing Company model.
     * If deletion is successful, the browser will be redirected to the 'index' page.
     * @param integer $id
     * @return mixed
     * @throws NotFoundHttpException if the model cannot be found
     */
    public function actionDelete($id)
    {
        $company = $this->findModel($id);
        $company->status = Company::STATUS_DELETED;
        $company->update(false, ['status']);

        return $this->redirect(['index']);
    }

    /**
     * Finds the Company model based on its primary key value.
     * If the model is not found, a 404 HTTP exception will be thrown.
     * @param integer $id
     * @return Company the loaded model
     * @throws NotFoundHttpException if the model cannot be found
     */
    protected function findModel($id)
    {
        if (($model = Company::findOne($id)) !== null) {
            return $model;
        }

        throw new NotFoundHttpException('The requested page does not exist.');
    }

    public function actionCreateUser($company_id)
    {
        $company = $this->findModel($company_id);
        $signupForm = new SignupForm();
        $signupForm->scenario = 'dealerSignup';

        if ($signupForm->load(Yii::$app->request->post())) {
            if ($user = $signupForm->signup()) {
                if ($signupForm->role == User::ROLE_LEAD_MANAGER) {
                    $role = 'leadManager';
                } else {
                    $role = 'manager';
                }
                $auth = Yii::$app->authManager;
                $authorRole = $auth->getRole($role);
                $auth->assign($authorRole, $user->getId());

                $this->redirect(['view', 'id' => $signupForm->company_id]);
            }
        }

        return $this->render('createUser', [
            'company' => $company,
            'signupForm' => $signupForm
        ]);
    }

    public function actionUpdateUser($id)
    {
        $user = User::findOne($id);
        if (!$user) {
            throw new HttpException(404, 'Пользователь не найден');
        }
        $signupForm = new SignupForm();
        $signupForm->scenario = 'editUser';
        $signupForm->initByUser($user);
        $userCompany = $user->company;

        if ($signupForm->load(Yii::$app->request->post())) {
            if ($user = $signupForm->saveWithAssignedUser()) {
                $auth = Yii::$app->authManager;
                $userRoles = $auth->getRolesByUser($user->id);
                foreach ($userRoles as $userRole) {
                    $auth->revoke($userRole, $user->id);
                }
                if ($signupForm->role == User::ROLE_LEAD_MANAGER) {
                    $role = 'leadManager';
                } else {
                    $role = 'manager';
                }
                $authorRole = $auth->getRole($role);
                $auth->assign($authorRole, $user->getId());

                $this->redirect(['view', 'id' => $signupForm->company_id]);
            }
        }

        return $this->render('updateUser', [
            'company' => $userCompany,
            'signupForm' => $signupForm
        ]);
    }

    public function actionDeleteUser($id)
    {
        $user = User::findOne($id);
        if (!$user) {
            throw new HttpException(404, 'Not found');
        }
        $user->status = User::STATUS_DELETED;
        $user->update(false, ['status']);

        return $this->redirect(['view', 'id' => $user->company_id]);
    }
}
